#Elaborar um algoritmo que efetue a leitura de três números inteiros e apresente a soma dos seus quadrados.

num = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))
num3 = int(input("Digite o terceiro número: "))
somaqua = (num ** 2) + (num2 ** 2)  (num3 ** 2)
print(f"A soma dos quadrados de {num},{num2} e {num3} é {somaqua}")